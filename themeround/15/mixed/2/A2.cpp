using ll = long long;

void solve() {
  int n, m, k;
  cin >> n >> m >> k;

  if (n - (n + m - 1) / m > k)
    cout << "YES" << "\n";
  else
    cout << "NO" << "\n";
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);

  int t;
  cin >> t;

  while (t--)
    solve();

  return 0;
}
